// 汉诺塔实现原理
// 这个程序使用递归思想解决汉诺塔问题。核心思路是将复杂问题分解为更简单的子问题：
// 当需要移动 n 个盘子时，程序将其分解为三个步骤：
// 首先将上面的 n-1 个盘子从起始柱借助目标柱移动到辅助柱（通过递归调用 move(n-1, c1, c3, c2) 实现）
// 然后将最大的第 n 个盘子直接从起始柱移动到目标柱（通过 printf("%c->%c\n", c1, c3) 输出移动步骤）
// 最后将 n-1 个盘子从辅助柱借助起始柱移动到目标柱（通过递归调用 move(n-1, c2, c1, c3) 实现）
// 递归的基础情况是当 n=1 时，直接将盘子从起始柱移动到目标柱。通过这种递归分解，无论有多少个盘子,
// 都可以逐步简化为移动单个盘子的基本操作，从而完成整个汉诺塔问题的求解。

#include <stdio.h>
void move(int n, char c1, char c2, char c3);
int main()
{
    int n;
    scanf("%d", &n);
    move(n, 'a', 'b', 'c');
}

void move(int n, char c1, char c2, char c3)
{
    if (n == 1)
    {
        printf("%c->%c\n", c1, c3);
    }
    else
    {
        move(n - 1, c1, c3, c2);
        printf("%c->%c\n", c1, c3);
        move(n - 1, c2, c1, c3);
    }
}